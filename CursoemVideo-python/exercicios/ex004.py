a = input('Digite algo: ')
print('O tipo primitivo desse valor é {}' .format(type(a)))
print('Contém só espaços? {}' .format(a.isspace()))
print('É um número? {}' .format(a.isnumeric()))
print(('É alfabético? {}' .format(a.isalpha())))
print('É um alfanumérico? {}' .format(a.isalnum()))
print('Está Maiúsculo? {}' .format(a.isupper()))
print('Está minúsculo? {}' .format(a.islower()))
print('Está Capitalizada? {}' .format(a.istitle()))






